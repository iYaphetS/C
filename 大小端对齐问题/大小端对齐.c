#include <stdio.h>

//在IP和整数之间相互转化-----数组和整型数据在内存中存储方式是完全相反
//只因为数组是前面的元素的数据的低位，而整数后面（个位）才是数据的低位

union 
{
	int a;
	char b[2];

}s;
int main()
{
	s.b[0] = 10; 
	s.b[1] = 1;

	printf("%d\n", s.a);
	char *p = &s.a;
	printf("%02x, %02x, %02x, %02x", p[0], p[1], p[2], p[3]);
	//低地址 ----- 高地址  
	//0a  (低位), 01, 00, 00  (高位) ---- 数组形式
	//00  (高位)  00  01  0a （低位）---- 整数形式
	return 0;
}